package baitaplon.dto;

public class NganhDTO {
	private String ID;
	private String ten_nganh;
	private String ID_khoa;

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public String getTen_nganh() {
		return ten_nganh;
	}

	public void setTen_nganh(String ten_nganh) {
		this.ten_nganh = ten_nganh;
	}

	public String getID_khoa() {
		return ID_khoa;
	}

	public void setID_khoa(String iD_khoa) {
		ID_khoa = iD_khoa;
	}

}
